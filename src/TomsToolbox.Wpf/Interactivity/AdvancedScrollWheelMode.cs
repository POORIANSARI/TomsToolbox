namespace TomsToolbox.Wpf.Interactivity;

/// <summary>
/// Specifies the smooth scrolling mode.
/// </summary>
public enum AdvancedScrollWheelMode
{
    /// <summary>
    /// Smooth scrolling is turned off.
    /// </summary>
    None,
    /// <summary>
    /// Smooth scrolling with animation.
    /// </summary>
    WithAnimation,
    /// <summary>
    /// Smooth scrolling without animation.
    /// </summary>
    WithoutAnimation
}
